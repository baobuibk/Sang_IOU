int16_t get_NTC_temperature_using_table(uint8_t channel){
	uint16_t ADCVal  = adc_read(channel);
	return pgm_read_word(&NTC_table[ADCVal]);
};

int16_t get_NTC_temperature(uint8_t channel)
{
	uint16_t	ADCVal  = adc_read(channel);
	return ntc_convertToC_10times(ADCVal);
}

float ntc_convertToC(uint16_t adcValue)
{
	float rntc = (((double)_NTC_R_SERIES * 5  / (5 * adcValue)) * (double)_NTC_ADC_MAX) - 10000;
	float temp;
	temp = rntc / (float)_NTC_R_NOMINAL;
	temp = logf(temp);
	temp /= (float)_NTC_BETA;
	temp += 1.0f / ((float)_NTC_TEMP_NOMINAL + 273.15f);
	temp = 1.0f / temp;
	temp -= 273.15f;
	return (float)temp;
}

int16_t ntc_convertToC_10times(uint16_t adcValue)
{
	return	10*ntc_convertToC(adcValue);
}